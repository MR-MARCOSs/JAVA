
public class Acompanhantes {
 
    private String nomeAc;
    private String parentesco;
    private double rgAc;
    private int apto;
    

    public String getNomeAc() {
        return nomeAc;
    }
    public void setNomeAc(String nomeAc) {
        this.nomeAc = nomeAc;
    }
    public String getParentesco() {
        return parentesco;
    }
    public void setParentesco(String parentesco) {
        this.parentesco = parentesco;
    }
    public double getRgAc() {
        return rgAc;
    }
    public void setRgAc(double rgAc) {
        this.rgAc = rgAc;
    }
    public int getApto() {
        return apto;
    }
    public void setApto(int apto) {
        this.apto = apto;
    }

    

}
